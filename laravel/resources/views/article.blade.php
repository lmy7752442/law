		<p align="center">中华人民共和国婚姻法（修正）</p>
		<p>　　（1980年9月10日第五届全国人民代表大会第三次会议通过 根据2001年4月28日第九届全国人民代表大会常务委员会第二十一次会议《关于修改〈中华人民共和国婚姻法〉的决定》修正）</p>
		<p>　　目录</p>
		<p>　　第一章 总则</p>
		<p>　　第二章 结婚</p>
		<p>　　第三章 家庭关系</p>
		<p>　　第四章 离婚</p>
		<p>　　第五章 救助措施与法律责任</p>
		<p>　　第六章 附则</p>
		<p>&nbsp;</p>
		<p>　　第一章 总则</p>
		<p>　　第一条 本法是婚姻家庭关系的基本准则。</p>
		<p>　　第二条 实行婚姻自由、一夫一妻、男女平等的婚姻制度。</p>
		<p>　　保护妇女、儿童和老人的合法权益。</p>
		<p>　　实行计划生育。</p>
		<p>　　第三条 禁止包办、买卖婚姻和其他干涉婚姻自由的行为。禁止借婚姻索取财物。</p>
		<p>　　禁止重婚。禁止有配偶者与他人同居。禁止家庭暴力。禁止家庭成员间的虐待和遗弃。</p>
		<p>　　第四条 夫妻应当互相忠实，互相尊重；家庭成员间应当敬老爱幼，互相帮助，维护平等、和睦、文明的婚姻家庭关系。</p>
		<p>　　第二章 结婚</p>
		<p>　　第五条 结婚必须男女双方完全自愿，不许任何一方对他方加以强迫或任何第三者加以干涉。</p>
		<p>　　第六条 结婚年龄，男不得早于二十二周岁，女不得早于二十周岁。晚婚晚育应予鼓励。</p>
		<p>　　第七条 有下列情形之一的，禁止结婚：</p>
		<p>　　（一）直系血亲和三代以内的旁系血亲；</p>
		<p>　　（二）患有医学上认为不应当结婚的疾病。</p>
		<p>　　第八条 要求结婚的男女双方必须亲自到婚姻登记机关进行结婚登记。符合本法规定的，予以登记，发给结婚证。取得结婚证，即确立夫妻关系。未办理结婚登记的，应当补办登记。</p>
		<p>　　第九条 登记结婚后，根据男女双方约定，女方可以成为男方家庭的成员，男方可以成为女方家庭的成员。</p>
		<p>　　第十条 有下列情形之一的，婚姻无效：</p>
		<p>　　（一）重婚的；</p>
		<p>　　（二）有禁止结婚的亲属关系的；</p>
		<p>　　（三）婚前患有医学上认为不应当结婚的疾病，婚后尚未治愈的；</p>
		<p>　　（四）未到法定婚龄的。</p>
		<p>　　第十一条 因胁迫结婚的，受胁迫的一方可以向婚姻登记机关或人民法院请求撤销该婚姻。受胁迫的一方撤销婚姻的请求，应当自结婚登记之日起一年内提出。被非法限制人身自由的当事人请求撤销婚姻的，应当自恢复人身自由之日起一年内提出。</p>
		<p>　　第十二条 无效或被撤销的婚姻，自始无效。当事人不具有夫妻的权利和义务。同居期间所得的财产，由当事人协议处理；协议不成时，由人民法院根据照顾无过错方的原则判决。对重婚导致的婚姻无效的财产处理，不得侵害合法婚姻当事人的财产权益。当事人所生的子女，适用本法有关父母子女的规定。</p>
		<p>　　第三章 家庭关系</p>
		<p>　　第十三条 夫妻在家庭中地位平等。</p>
		<p>　　第十四条 夫妻双方都有各用自己姓名的权利。</p>
		<p>　　第十五条 夫妻双方都有参加生产、工作、学习和社会活动的自由，一方不得对他方加以限制或干涉。</p>
		<p>　　第十六条 夫妻双方都有实行计划生育的义务。</p>
		<p>　　第十七条 夫妻在婚姻关系存续期间所得的下列财产，归夫妻共同所有：</p>
		<p>　　（一）工资、奖金；</p>
