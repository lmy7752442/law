	<div class="fagui_text">
		<p>　　《全国人民代表大会常务委员会关于修改〈中华人民共和国婚姻法〉的决定》已由中华人民共和国第九届全国人民代表大会常务委员会第二十一次会议于2001年4月28日通过，现予公布，自公布之日起施行。</p>
